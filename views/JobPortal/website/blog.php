<script>
    document.addEventListener('DOMContentLoaded', function() {
        const blogModal = document.getElementById('blogModal');
        
        if (blogModal) {
            blogModal.addEventListener('show.bs.modal', function(event) {
                // Button that triggered the modal
                const button = event.relatedTarget;
                
                // Extract info from data attributes
                const articleId = button.getAttribute('data-article');
                
                // Find the article content
                const contentSource = document.getElementById(`blog-content-${articleId}`);
                
                if (contentSource) {
                    // Update the modal's content
                    const modalTitle = blogModal.querySelector('.modal-title');
                    const modalContent = blogModal.querySelector('.blog-content-container');
                    
                    // Get title from the card
                    const articleTitle = button.closest('.card-body').querySelector('h5').textContent;
                    modalTitle.textContent = articleTitle;
                    
                    // Copy content from hidden div
                    modalContent.innerHTML = contentSource.innerHTML;
                } else {
                    blogModal.querySelector('.blog-content-container').innerHTML = '<div class="alert alert-danger">Article content not found.</div>';
                }
            });
            
            // Clear modal when hidden
            blogModal.addEventListener('hidden.bs.modal', function() {
                blogModal.querySelector('.blog-content-container').innerHTML = '';
            });
        }
    });
</script>